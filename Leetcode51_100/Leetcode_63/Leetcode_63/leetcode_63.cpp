//solution 1: Time Limit Exceeded
// Calc all the routes;
class Solution1 {
public:
	int uniquePathsWithObstacles(vector<vector<int>>& obstacleGrid) {
		int m = obstacleGrid.size();
		int n = obstacleGrid.begin()->size();
		return get_solution_num(obstacleGrid, m, n);
	}
	int get_solution_num(std::vector<std::vector<int>> & obstacle, int start_m, int start_n) {
		if (start_m == 1 && start_n == 1) {
			return obstacle[0][0] == 1 ? 0 : 1;
		}
		else if (obstacle[start_m - 1][start_n - 1] == 1) {
			return 0;
		}
		else {
			if (start_m == 1) {
				return get_solution_num(obstacle, start_m, start_n - 1);
			}
			else if (start_n == 1) {
				return get_solution_num(obstacle, start_m - 1, start_n);
			}
			else {
				return get_solution_num(obstacle, start_m - 1, start_n) + get_solution_num(obstacle, start_m, start_n - 1);
			}
		}
	}
};

//solution 2: Use DP;
//State transition formulation:
//zpath_num[row][col] =path_num[row - 1][col] + path_num[row][col - 1]
class Solution {
public:
	int uniquePathsWithObstacles(vector<vector<int>>& obstacleGrid) {
		//Caution: long int must replace int or the interger will overload;
		//Actually, the space complexity can be reduce to O(1) but not O(M*N);
		//Because the (row, col) element only relies on row-1_th row and col-1_th column.
		std::vector<std::vector<long int>> path_num(obstacleGrid.size(), std::vector<long int>(obstacleGrid.begin()->size(), 0));
		for (int row = 0; row < obstacleGrid.size(); ++row) {
			for (int col = 0; col < obstacleGrid.begin()->size(); ++col) {
				if (row == 0 && col == 0) {
					path_num[row][col] = obstacleGrid[row][col] == 1 ? 0 : 1;
				}
				else if(row==0) {
					path_num[row][col] = obstacleGrid[row][col] == 1 ? 0 : path_num[row][col - 1];
				}
				else if (col == 0) {
					path_num[row][col] = obstacleGrid[row][col] == 1 ? 0 : path_num[row - 1][col];
				}
				else {
					path_num[row][col] = obstacleGrid[row][col] == 1 ? 0 : (path_num[row - 1][col] + path_num[row][col - 1]);
				}
			}
		}
		return path_num[obstacleGrid.size() - 1][obstacleGrid.begin()->size() - 1];
	}
};

